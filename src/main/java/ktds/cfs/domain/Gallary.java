package ktds.cfs.domain;

public class Gallary {
  protected int no;
  protected String writer, u;
  public int getNo() {
    return no;
  }
  public void setNo(int no) {
    this.no = no;
  }
  public String getWriter() {
    return writer;
  }
  public void setWriter(String writer) {
    this.writer = writer;
  }
  public String getU() {
    return u;
  }
  public void setU(String u) {
    this.u = u;
  }
}